let currentJailbreakIndex = 0;

function renderFreeProducts() {
    const grid = document.getElementById('freeProductsGrid');
    if (!grid) {
        console.log("freeProductsGrid tidak ditemukan");
        return;
    }
    
    renderJailbreakCarousel();
}

function renderJailbreakCarousel() {
    const grid = document.getElementById('freeProductsGrid');
    if (!grid) return;
    
    grid.innerHTML = `
        <div class="jailbreak-container">
            <div class="jailbreak-header">
                <i class="ri-skull-line"></i>
                <h3>Prompt Jail Break</h3>
                <p>Geser ke kiri/kanan untuk pilih prompt, klik untuk salin</p>
            </div>
            
            <div class="jailbreak-carousel">
                <button class="carousel-nav prev" onclick="prevJailbreak()">
                    <i class="ri-arrow-left-s-line"></i>
                </button>
                
                <div class="jailbreak-card" id="jailbreakCard" onclick="copyJailbreakPrompt()">
                    <div class="jailbreak-name" id="jailbreakName"></div>
                    <div class="jailbreak-work" id="jailbreakWork"></div>
                    <div class="jailbreak-prompt" id="jailbreakPrompt"></div>
                    <div class="jailbreak-copy">
                        <i class="ri-file-copy-line"></i> Klik untuk salin
                    </div>
                </div>
                
                <button class="carousel-nav next" onclick="nextJailbreak()">
                    <i class="ri-arrow-right-s-line"></i>
                </button>
            </div>
            
            <div class="jailbreak-dots" id="jailbreakDots"></div>
        </div>
    `;
    
    updateJailbreakDisplay();
}

function updateJailbreakDisplay() {
    const card = document.getElementById('jailbreakCard');
    
    // Tambah animasi fade out
    card.classList.add('fade-out');
    
    setTimeout(() => {
        const prompt = jailbreakPrompts[currentJailbreakIndex];
        document.getElementById('jailbreakName').innerText = prompt.name;
        document.getElementById('jailbreakWork').innerHTML = `<i class="ri-smartphone-line"></i> Work: ${prompt.work}`;
        document.getElementById('jailbreakPrompt').innerText = prompt.prompt;
        
        // Hapus fade out
        card.classList.remove('fade-out');
        
        const dotsContainer = document.getElementById('jailbreakDots');
        if (dotsContainer) {
            dotsContainer.innerHTML = jailbreakPrompts.map((_, i) => `
                <span class="dot ${i === currentJailbreakIndex ? 'active' : ''}" onclick="goToJailbreak(${i})"></span>
            `).join('');
        }
        
        // Toast notifikasi posisi
        showPositionToast(currentJailbreakIndex + 1, jailbreakPrompts.length, prompt.name);
    }, 200);
}

function nextJailbreak() {
    // Loop ke awal jika sudah di akhir
    if (currentJailbreakIndex >= jailbreakPrompts.length - 1) {
        currentJailbreakIndex = 0;
    } else {
        currentJailbreakIndex++;
    }
    updateJailbreakDisplay();
}

function prevJailbreak() {
    // Loop ke akhir jika sudah di awal
    if (currentJailbreakIndex <= 0) {
        currentJailbreakIndex = jailbreakPrompts.length - 1;
    } else {
        currentJailbreakIndex--;
    }
    updateJailbreakDisplay();
}

function goToJailbreak(index) {
    currentJailbreakIndex = index;
    updateJailbreakDisplay();
}

function copyJailbreakPrompt() {
    const prompt = jailbreakPrompts[currentJailbreakIndex].prompt;
    navigator.clipboard.writeText(prompt).then(() => {
        showToast(`✅ Prompt "${jailbreakPrompts[currentJailbreakIndex].name}" berhasil disalin!`);
    }).catch(() => {
        showToast('❌ Gagal menyalin prompt', true);
    });
}

function showPositionToast(current, total, name) {
    // Hapus toast lama jika ada
    const oldToast = document.querySelector('.prompt-position-toast');
    if (oldToast) oldToast.remove();
    
    const toast = document.createElement('div');
    toast.className = 'prompt-position-toast';
    toast.innerHTML = `<i class="ri-information-line"></i> ${current} / ${total} - ${name}`;
    document.body.appendChild(toast);
    
    setTimeout(() => {
        if (toast) toast.remove();
    }, 2500);
}